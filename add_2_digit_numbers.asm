ASSUME DS:DATA,CS:CODE

DATA SEGMENT
prompt1 DB 0AH,0DH,"ENTER FIRST NUMBER:$"
prompt2 DB 0AH,0DH,"ENTER SECOND NUMBER:$"
prompt3 DB 0AH,0DH,"THE ANSWER IS:$"
DATA ENDS

CODE SEGMENT
start:
	MOV AX,DATA 
	MOV DS,AX

	LEA DX,prompt1
	MOV AH,09H
	INT 21H

	MOV AH,01H
	INT 21H
	MOV CH,AL
	MOV AH,01H
	INT 21H
	MOV CL,AL

	LEA DX,prompt2
	MOV AH,09H
	INT 21H

	MOV AH,01H
	INT 21H
	MOV BH,AL
	MOV AH,01H
	INT 21H
	MOV BL,AL
	
	LEA DX,	prompt3
	MOV AH,09H
	INT 21H

	MOV AH,00H 
	ADD BL,CL
	MOV AL,BL  
	AAA	   
	MOV CL,AH  ;CARRY OF LOWER DIGIT
	ADD AL,30H 
	MOV BL,AL  ;ANSWER PART 1

	MOV AH,00H 
	ADD BH,CH
	MOV AL,BH
	AAA
	ADD AH,30H
	MOV BH,AH  ;ANSWER PART 3
	ADD AL,CL
	ADD AL,30H
	MOV CH,AL  ;ANSWER PART 2

	MOV DL,BH 
	MOV AH,02H
	INT 21H

	MOV DL,CH 
	MOV AH,02H
	INT 21H

	MOV DL,BL
	MOV AH,02H
	INT 21H

	MOV AH,4CH 
	INT 21H

CODE ENDS
END START